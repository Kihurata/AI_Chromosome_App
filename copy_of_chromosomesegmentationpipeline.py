def _watershed_fallback(crop_bgr):
    gray = cv2.cvtColor(crop_bgr, cv2.COLOR_BGR2GRAY)
    _, thr = cv2.threshold(gray, 10, 255, cv2.THRESH_BINARY)
    k  = np.ones((3,3), np.uint8)
    op = cv2.morphologyEx(thr, cv2.MORPH_OPEN, k, iterations=2)
    bg = cv2.dilate(op, k, iterations=3)
    dist = cv2.distanceTransform(op, cv2.DIST_L2, 5)
    if dist.max() == 0: return [thr]
    _, fg  = cv2.threshold(dist, 0.6*dist.max(), 255, 0)
    fg     = np.uint8(fg)
    unk    = cv2.subtract(bg, fg)
    ret, markers = cv2.connectedComponents(fg)
    markers = markers+1; markers[unk==255] = 0
    markers = cv2.watershed(crop_bgr.copy(), markers)
    res = []
    for i in range(2, ret+1):
        m = (markers==i).astype(np.uint8)*255
        cnts,_ = cv2.findContours(m, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        if cnts and cv2.contourArea(max(cnts, key=cv2.contourArea)) >= MIN_CHR_AREA:
            res.append(m)
    return res if res else [thr]

def sep_clustered(crop_bgr):
    gray  = cv2.cvtColor(crop_bgr, cv2.COLOR_BGR2GRAY)
    img_n = csbdeep_normalize(gray.astype(np.float32)/255.0, 1, 99.8)
    try:
        labels, _ = stardist_model.predict_instances(img_n)
    except Exception:
        return _watershed_fallback(crop_bgr)
    if labels.max() == 0:
        return _watershed_fallback(crop_bgr)
    k = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (5,5))
    masks = []
    for i in range(1, int(labels.max())+1):
        m = (labels==i).astype(np.uint8)*255
        if m.sum()//255 < MIN_CHR_AREA: continue
        m = cv2.morphologyEx(m, cv2.MORPH_CLOSE, k)
        masks.append(m)
    return masks if masks else _watershed_fallback(crop_bgr)

