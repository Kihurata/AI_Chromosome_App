def remove_background(img_path):
    pil  = Image.open(img_path).convert("RGB")
    W, H = pil.size
    t    = bg_tf(pil).unsqueeze(0).to(DEVICE)
    with torch.no_grad():
        out = bg_model(t)
    mask = torch.sigmoid(out).squeeze().cpu().numpy()
    mask = (mask > BG_THRESHOLD).astype(np.uint8) * 255
    mask = np.array(Image.fromarray(mask).resize((W, H), Image.Resampling.LANCZOS))
    k    = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (BG_MORPH, BG_MORPH))
    mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, k)
