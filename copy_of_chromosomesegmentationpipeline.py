    mask = (mask > 127).astype(np.uint8) * 255
    orig = np.array(pil)
    res  = np.zeros_like(orig)
    res[mask > 127] = orig[mask > 127]
    return cv2.cvtColor(res, cv2.COLOR_RGB2BGR)

