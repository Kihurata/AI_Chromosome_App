def keep_largest(mask):
    nl, labels, stats, _ = cv2.connectedComponentsWithStats(mask, connectivity=8)
    if nl <= 1: return mask
    lg = 1 + np.argmax(stats[1:, cv2.CC_STAT_AREA])
    r  = np.zeros_like(mask); r[labels == lg] = 255
    return r

