def classify_all(crop_list, batch_size=64):
    all_probs, buf = [], []
    for i, crop in enumerate(crop_list):
        img = cv2.cvtColor(crop, cv2.COLOR_BGR2RGB)
        buf.append(cls_tf(image=img)["image"])
        if len(buf) == batch_size or i == len(crop_list)-1:
            batch = torch.stack(buf).to(DEVICE)
            with torch.no_grad():
                p = F.softmax(cls_model(batch), dim=1).cpu().numpy()
            all_probs.append(p); buf = []
    return np.vstack(all_probs)   # (N, 24)

# ── 11. HUNGARIAN ASSIGNMENT ──────────────────────────────────────────────────
def assign_pairs(probs, crop_list):
    N = len(crop_list)
    if N == 0: return {n: [] for n in CLASS_NAMES}, []

    y_sum = probs[:, 23].sum()
    sex   = "male" if y_sum > 0.3 else "female"

    exp = {str(i): 2 for i in range(1, 23)}
    exp["X"] = 2 if sex == "female" else 1
    exp["Y"] = 0 if sex == "female" else 1

    slots = []
    for name in CLASS_NAMES:
        for _ in range(max(1, exp.get(name, 1))):
            slots.append(name)
    M = len(slots)

    slot_ci = [CLASS_NAMES.index(s) for s in slots]
    cost    = np.zeros((N, M), dtype=np.float64)
    for j, ci in enumerate(slot_ci):
        cost[:, j] = -np.log(probs[:, ci] + 1e-9)
    if N > M:
        cost = np.hstack([cost, np.full((N, N-M), 100.0)])

    row_ind, col_ind = linear_sum_assignment(cost)
    assignment = {n: [] for n in CLASS_NAMES}
    unassigned = []
    for r, c in zip(row_ind, col_ind):
        if c < M: assignment[slots[c]].append(crop_list[r])
        else:     unassigned.append(crop_list[r])
    return assignment, unassigned

# ── 12. ORIENT ────────────────────────────────────────────────────────────────
def orient_chromosome(crop_bgr):
    gray = cv2.cvtColor(crop_bgr, cv2.COLOR_BGR2GRAY)
    _, mask = cv2.threshold(gray, 10, 255, cv2.THRESH_BINARY)
    pts = np.column_stack(np.where(mask > 0)).astype(np.float32)
    if len(pts) < 10: return crop_bgr

    mean = pts.mean(axis=0)
    _, _, vt = np.linalg.svd(pts - mean, full_matrices=False)
    angle_deg    = np.degrees(np.arctan2(vt[0][1], vt[0][0]))
    rotate_angle = angle_deg - 90.0

    H, W = crop_bgr.shape[:2]
    cx, cy = W/2, H/2
    M_rot  = cv2.getRotationMatrix2D((cx, cy), -rotate_angle, 1.0)
    cos_a, sin_a = abs(M_rot[0,0]), abs(M_rot[0,1])
    nW = int(H*sin_a + W*cos_a); nH = int(H*cos_a + W*sin_a)
    M_rot[0,2] += nW/2-cx; M_rot[1,2] += nH/2-cy
    rotated = cv2.warpAffine(crop_bgr, M_rot, (nW,nH),
                              flags=cv2.INTER_LANCZOS4,
                              borderMode=cv2.BORDER_CONSTANT, borderValue=255)

    # Crop tight
    g2 = cv2.cvtColor(rotated, cv2.COLOR_BGR2GRAY)
    _, m2 = cv2.threshold(g2, 240, 255, cv2.THRESH_BINARY_INV)
    ys, xs = np.where(m2 > 0)
    if len(xs) == 0: return rotated
    pad = 4
    x1c = max(0, xs.min()-pad); y1c = max(0, ys.min()-pad)
    x2c = min(rotated.shape[1], xs.max()+pad)
    y2c = min(rotated.shape[0], ys.max()+pad)
    cropped = rotated[y1c:y2c, x1c:x2c]

    # Centromere: min-width scan → p-arm lên trên
    g3 = cv2.cvtColor(cropped, cv2.COLOR_BGR2GRAY)
    _, m3 = cv2.threshold(g3, 240, 255, cv2.THRESH_BINARY_INV)
    H3 = m3.shape[0]
    row_w = []
    for r in range(H3):
        nz = np.where(m3[r] > 0)[0]
        row_w.append(nz[-1]-nz[0]+1 if len(nz)>1 else 0)
    row_w  = np.array(row_w, dtype=np.float32)
    ks     = max(3, H3//20)
    smooth = np.convolve(row_w, np.ones(ks)/ks, mode="same")
    margin = max(1, H3//10)
    region = smooth[margin:H3-margin]
    if len(region) > 0:
        centro = int(region.argmin()) + margin
        if centro > H3/2:
            cropped = cv2.flip(cropped, 0)
    return cropped

